function fn_ab__stickers_is_valid_cron_key($value, $check_equality = true)
{
return strlen($value) >= 10 && strlen($value) <= 20 && ($check_equality ? Registry::get('addons.ab__stickers.cron_key') === $value : true);
}

function fn_ab__stickers_get_storefront_id_by_company_id($company_id)
{
return (int) db_get_field('SELECT storefront_id FROM ?:storefronts_companies WHERE company_id = ?i', $company_id);
}
