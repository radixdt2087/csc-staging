use Tygh\Registry;
use Tygh\Enum\ObjectStatuses;
use Tygh\Enum\YesNo;
function fn_ab__mb_templates_geo_maps()
{
return (Registry::get('addons.geo_maps.status') === ObjectStatuses::ACTIVE) && YesNo::toBool(Registry::get('addons.geo_maps.show_shippings_on_product'));
}
function fn_ab__mb_templates_categories_list()
{
return db_get_field('SELECT COUNT(*) FROM ?:categories') > 1 ? true : false;
}
function fn_ab__mb_templates_tags()
{
return Registry::get('addons.tags.status') === ObjectStatuses::ACTIVE;
}